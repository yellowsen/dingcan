package com.sen.dingcan.bean;

public class Food {

	private String name;
	private String icon;
	private double price;

	private int icon2;
	
	
	
	public int getIcon2() {
		return icon2;
	}
	public void setIcon2(int icon2) {
		this.icon2 = icon2;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	
	
}
